package com.bridgelabz.DLS_Assinment_Program.Day5;

public class EvenOrOdd {
    public static void main(String[] args) {
        int n=13;
        if (n%2==0){
            System.out.println("The number is even: " + n);
        }
        else {
            System.out.println("Number is odd: " + n);
        }
    }
}
